package com.consultoraestrategia.ss_crmeducativo.portal.rubroInformacion.entities;

public class CursoUi {

    private String nombre;
    private String seccion;
    private String periodo;
    private String colorCurso;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSeccion() {
        return seccion;
    }

    public void setSeccion(String seccion) {
        this.seccion = seccion;
    }

    public String getPeriodo() {
        return periodo;
    }

    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }

    public String getColorCurso() {
        return colorCurso;
    }

    public void setColorCurso(String colorCurso) {
        this.colorCurso = colorCurso;
    }
}
